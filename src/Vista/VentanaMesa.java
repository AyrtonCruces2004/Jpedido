    public VentanaMesa(String n_mesa, int id_usuario, int id_sucursal, int id_mesa) {
        initComponents();
        lblnombre.setText(n_mesa);
        id_suc = id_sucursal;
        id_mesa2 = id_mesa;
        nombre_mesa = n_mesa;

        DefaultListModel<String> listModel = new DefaultListModel<>();
        DefaultListModel<String> listModel2 = new DefaultListModel<>();
        DefaultListModel<String> listModel3 = new DefaultListModel<>();
        DefaultListModel<String> listModel4 = new DefaultListModel<>();

        jListentradas.setModel(listModel);
        jListplato.setModel(listModel2);
        jlistbebidas.setModel(listModel3);
        jlistpostres.setModel(listModel4);

        productos = cAdmin.ListarProductos(id_sucursal);

        for (Producto producto : productos) {

            if (producto.getId_cat() == 1) {
                listModel.addElement((String) producto.getNom_Producto());
            }
            if (producto.getId_cat() == 2) {
                listModel2.addElement((String) producto.getNom_Producto());
            }
            if (producto.getId_cat() == 3) {
                listModel3.addElement((String) producto.getNom_Producto());
            }
            if (producto.getId_cat() == 4) {
                listModel4.addElement((String) producto.getNom_Producto());
            }

        }

        pedidocompleto = cAdmin.Listarpedidomesa(id_mesa);

        for (PedidoProd pedidoProd : pedidocompleto) {
            m_pedido += pedidoProd.getNom_Prod() + "\n";
            txtPedido.setText(m_pedido);
        }

        if (pedidocompleto.size() > 0) {
            id_pedido = pedidocompleto.get(0).getId_pedido();
        } else {
            id_pedido = cAdmin.UltimoIDPedido(id_mesa) + 1;
        }

        if (txtPedido.getText().length() > 0) {
            opcion = "editar";
        }
    }

