    private void agregar_producto(JList<String> lista) {
        String select_producto = lista.getSelectedValue();
        if (select_producto != null) {
            String nombre_producto = select_producto;
            m_pedido += nombre_producto + "\n";
            int id_producto = cAdmin.GetIDProducto(nombre_producto, id_suc);
            Optional<Producto> producto  = productos.stream().filter(producto2 -> producto2.getId_Producto() == id_producto).findFirst();
            double precio = producto.get().getPrecio_Producto();
            int tiempo = producto.get().getTime_Producto();
            
            pedidonuevo.add(new PedidoProd(id_pedido, id_producto, nombre_producto,precio, id_mesa2,tiempo));
            pedidocompleto.add(new PedidoProd(id_pedido, id_producto, nombre_producto,precio, id_mesa2,tiempo));
        }
    }

