    public void mostrar(String resumen_diario) {
        // Asegúrate de que 'resumen_diario' tiene un nombre válido y seguro para la consulta
        String sql = "SELECT Fecha, Total_Cost FROM resumen_diario " + resumen_diario;
        Statement st;
        Conexion con = new Conexion();
        Connection conexion = null;
        try {
            conexion = con.getConnection();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, "Error de conexión: " + e.toString());
            return;
        }
        System.out.println(sql);

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Fecha");
        model.addColumn("Total_Cost");
        Fechas.setModel(model);

        String[] datos = new String[2]; // Necesitamos dos espacios para las columnas "Fecha" y "Total_Cost"
        try {
            st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("Fecha");  // Obtener la columna "Fecha"
                datos[1] = rs.getString("Total_Cost"); // Obtener la columna "Total_Cost"
                model.addRow(datos);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.toString());
        }
    }

