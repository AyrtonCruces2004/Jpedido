        if (selectedRow != -1) {
            String selectedDate = Fechas.getValueAt(selectedRow, 0).toString();
            String selectedtotal = Fechas.getValueAt(selectedRow, 1).toString();
            setVisible(false);

            DetalleVenta detalleVenta = new DetalleVenta(selectedDate,Double.parseDouble(selectedtotal));
            p_padre.add(detalleVenta);
            detalleVenta.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fecha.");
        }
