    public VistaDespacho(int id_sucursal) {
        initComponents();
        CAdmin admin = new CAdmin();

        List<PedidoFinal> pedidos = admin.ListarPedidos(id_sucursal);

        List<Integer> ids_pedidos = pedidos.stream().map(PedidoFinal::getID_Ped).distinct().collect(Collectors.toList());

        int xpanel = 20;
        int ypanel = 20;

        for (int id_pedido : ids_pedidos) {

            int contar = 1;
            List<PedidoFinal> pedidoFinals = pedidos.stream().filter(persona -> persona.getID_Ped() == id_pedido).collect(Collectors.toList());
            int sizepanel = CrearPedido(pedidoFinals, xpanel, ypanel);
            xpanel += 320;
            if (contar % 2 == 0 & contar > 0) {
                xpanel = 20;
                ypanel += sizepanel + 50;
            }
            contar++;
        }

    }

